#define pr_fmt(fmt) "sunxi: " fmt

#include <linux/module.h>
#include <linux/io.h>

#include <plat/platform.h>
#include <plat/system.h>

#define SRAMC_CHIP_ID_REG	(SW_VA_SRAM_IO_BASE + 0x24)

#define SRAMC_CHIP_ID_EN_MASK	0x1
#define SRAMC_CHIP_ID_EN_OFF	15
#define SRAMC_CHIP_ID_EN	(SRAMC_CHIP_ID_EN_MASK<<SRAMC_CHIP_ID_EN_OFF)

#define SRAMC_CHIP_ID_MASK	0xffff
#define SRAMC_CHIP_ID_OFF	16
#define SRAMC_CHIP_ID		(SRAMC_CHIP_ID_MASK<<SRAMC_CHIP_ID_OFF)

/*
 * BROM
 */
struct brom_header {
	u32 jump_instruction;	/* one intruction jumping to real code */
	char magic[8];		/* ="eGON.BRM",  not C-style string. */
	u32 length;		/* generated by PC */
	char Boot_vsn[4];	/* Boot version */
	char eGON_vsn[4];	/* eGON version */
	char platform[8];	/* platform information */
};

static inline const struct brom_header *_sunxi_brom(void)
{
	const struct brom_header *brom = (void*)SW_VA_BROM_BASE + 0x4000;
	if (strncmp(brom->magic, "eGON.BRM", 8) == 0)
		return brom;
	else
		return NULL;
}

int sunxi_pr_brom(void)
{
	const struct brom_header *brom = _sunxi_brom();
	if (brom) {
		pr_info("BROM Ver: %.4s %.4s %.8s\n",
			brom->Boot_vsn,
			brom->eGON_vsn,
			brom->platform);
		return 1;
	}
	pr_err("BROM not found\n");
	return 0;
}

u32 sunxi_brom_chip_id(void)
{
	const struct brom_header *brom = _sunxi_brom();
	if (brom) {
		char *endptr;
		u32 chip_id = simple_strtoul(brom->platform, &endptr, 10);
		if (*endptr == '\0')
			return chip_id;

		pr_err("BROM: failed to parse chip-id: \"%s\"\n",
		       brom->platform);
	}
	return SUNXI_UNKNOWN_MACH;
}
EXPORT_SYMBOL(sunxi_brom_chip_id);

/*
 * SRAMC
 */
u32 sunxi_sramc_chip_id(void)
{
	u32 chip_id, reg_val;

	/* enable chip_id reading */
	reg_val = readl(SRAMC_CHIP_ID_REG);
	writel(reg_val | SRAMC_CHIP_ID_EN, SRAMC_CHIP_ID_REG);

	reg_val = readl(SRAMC_CHIP_ID_REG);
	chip_id = ((reg_val&SRAMC_CHIP_ID)>>SRAMC_CHIP_ID_OFF) & SRAMC_CHIP_ID_MASK;

	switch (chip_id) {
	case 0x1623:
		return SUNXI_MACH_SUN4I;
	case 0x1625:
		return SUNXI_MACH_SUN5I;
	default:
		pr_err("SRAMC: failed to identify chip-id 0x%04x (*0x%08x == 0x%08x)\n",
		       chip_id, SRAMC_CHIP_ID_REG, reg_val);
		return SUNXI_UNKNOWN_MACH;
	}
}
EXPORT_SYMBOL(sunxi_sramc_chip_id);

/*
 */
u32 sunxi_chip_id(void)
{
	static u32 chip_id;

	if (unlikely(chip_id == 0))
		chip_id = sunxi_sramc_chip_id();

	return chip_id;
}
EXPORT_SYMBOL(sunxi_chip_id);

int sunxi_pr_chip_id(void)
{
	u32 chip_id = sunxi_chip_id();
	enum sw_ic_ver ver = sw_get_ic_ver();
	const char *soc_family = NULL;
	const char *name = NULL;
	int rev;

	if (sunxi_is_sun4i())
		soc_family = "sun4i";
	else if (sunxi_is_sun5i())
		soc_family = "sun5i";
	else
		soc_family = "sunNi?";

	switch (ver) {
	/* sun4i */
	case SUNXI_VER_A10A:
	case SUNXI_VER_A10B:
	case SUNXI_VER_A10C:
		name = "A10";
		rev = ver - SUNXI_VER_A10A;
		break;
	/* sun5i */
	case SUNXI_VER_A13A:
	case SUNXI_VER_A13B:
		name = "A13";
		rev = ver - SUNXI_VER_A13A;
		break;
	case SUNXI_VER_A12A:
	case SUNXI_VER_A12B:
		name = "A12";
		rev = ver - SUNXI_VER_A12A;
		break;
	case SUNXI_VER_A10SA:
	case SUNXI_VER_A10SB:
		name = "A10S";
		rev = ver - SUNXI_VER_A10SA;
		break;
	default:
		break;
	}

	pr_info("Allwinner %s revision %c (AW%u, %s) detected.\n",
		name?name:"A??", 'A' + rev, chip_id, soc_family);

	return name?1:0;
}

enum sw_ic_ver sw_get_ic_ver(void)
{
	static enum sw_ic_ver ver;

	if (likely(ver))
		return ver;

	if (sunxi_is_sun4i()) {
		u32 val = readl(SW_VA_TIMERC_IO_BASE + 0x13c);
		val = (val >> 6) & 0x3;

		if (val == 0)
			ver = SUNXI_VER_A10A;
		else if (val == 3)
			ver = SUNXI_VER_A10B;
		else
			ver = SUNXI_VER_A10C;
	} else if (sunxi_is_sun5i()) {
		u32 val = readl(SW_VA_SSE_IO_BASE);
		val = (val >> 16) & 0x07;

		if (val == 0)
			ver = SUNXI_VER_A13A;
		else if (val == 1) {
			val = readl(SW_VA_SID_IO_BASE+0x08);
			val = (val >> 12) & 0xf;

			if (val == 0 || val == 3)
				ver = SUNXI_VER_A12A;
			else if (val == 7)
				ver = SUNXI_VER_A10SA;
		}

		if (!ver)
			goto unknown_chip;

		val = readl(SW_VA_SID_IO_BASE+0x00);
		val = (val >> 8) & 0xffffff;

		if (val == 0 || val == 0x162541)
			; /* rev A */
		else if (val == 0x162542)
			ver++; /* rev B */
		else {
			const char *name;
			if (ver == SUNXI_VER_A13A)
				name = "A13";
			else if (ver == SUNXI_VER_A12A)
				name = "A12";
			else
				name = "A10S";

			pr_err("unrecongnized %s revision (%x)\n",
			       name, val);

			goto unknown;
		}
	}

	goto done;

unknown_chip:
	pr_err("unrecognized IC (chip-id=%u)\n", sunxi_chip_id());
unknown:
	ver = SUNXI_VER_UNKNOWN;
	{
		unsigned i;
		u32 reg = SW_VA_SID_IO_BASE;

		pr_err("secure-id dump (0x%08x):\n", reg);
		for (i=0; i<4; i++, reg += 0x04) {
			u32 v = readl(reg);
			pr_err("secure-id[%u]: 0x%08x\n", i, v);
		}
	}
done:
	return ver;
}
EXPORT_SYMBOL(sw_get_ic_ver);

int sw_get_chip_id(struct sw_chip_id *chip_id)
{
	chip_id->sid_rkey0 = readl(SW_VA_SID_IO_BASE);
	chip_id->sid_rkey1 = readl(SW_VA_SID_IO_BASE+0x04);
	chip_id->sid_rkey2 = readl(SW_VA_SID_IO_BASE+0x08);
	chip_id->sid_rkey3 = readl(SW_VA_SID_IO_BASE+0x0C);

	return 0;
}
EXPORT_SYMBOL(sw_get_chip_id);
